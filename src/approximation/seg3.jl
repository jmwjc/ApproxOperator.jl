
function set𝝭!(::Element{:Seg3},x::Node)
    ξ = x.ξ
    𝝭 = x[:𝝭]
    𝝭[1] = 0.5*ξ*(ξ-1.0)
    𝝭[2] = 0.5*ξ*(ξ+1.0)
    𝝭[3] = 1.0-ξ^2
end

function set∇𝝭!(ap::Element{:Seg3},x::Node)
    𝐽 = ap.𝐽
    ξ = x.ξ
    𝝭 = x[:𝝭]
    𝝭[1] = 0.5*ξ*(ξ-1.0)
    𝝭[2] = 0.5*ξ*(ξ+1.0)
    𝝭[3] = 1.0-ξ^2
    ∂𝝭∂x = x[:∂𝝭∂x]
    ∂𝝭∂x[1] = (ξ-0.5)*𝐽
    ∂𝝭∂x[2] = (ξ+0.5)*𝐽
    ∂𝝭∂x[3] = -2.0*ξ*𝐽
end