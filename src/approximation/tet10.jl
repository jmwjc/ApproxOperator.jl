
function set𝝭!(::Element{:Tet10},x::Node)
    ξ₂ = x.ξ
    ξ₃ = x.η
    ξ₄ = x.γ
    ξ₁ = 1.0 - ξ₂ - ξ₃ - ξ₄
    𝝭 = x[:𝝭]
   
    𝝭[1] = ξ₁*(2*ξ₁-1)
    𝝭[2] = ξ₂*(2*ξ₂-1)
    𝝭[3] = ξ₃*(2*ξ₃-1)
    𝝭[4] = ξ₄*(2*ξ₄-1)
    𝝭[5] = 4*ξ₁*ξ₂
    𝝭[6] = 4*ξ₂*ξ₃
    𝝭[7] = 4*ξ₃*ξ₁
    𝝭[8] = 4*ξ₁*ξ₄
    𝝭[9] = 4*ξ₃*ξ₄
    𝝭[10] = 4*ξ₂*ξ₄
end
